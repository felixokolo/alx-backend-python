#!/usr/bin/env python3

"""
Annotated concat function
"""

def concat(str1: str, str2: str) -> str:
    """
    Concats str1 and str2

    Parameters:
        str1 (str): first string
        str2 (str): second string

    Returns:
        concat (str): concat of str1 and str2
    """
    return str1 + str2